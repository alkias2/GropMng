namespace GropMng.Web.Framework.Models.DataTables;

/// <summary>
/// Static constants for CSS classes commonly used in DataTables grid columns.
/// </summary>
public static class GropColumnClassDefaults
{
    /// <summary>
    /// CSS class for center-aligning both horizontally and vertically.
    /// </summary>
    public const string CenterAll = "text-center align-middle";

    /// <summary>
    /// CSS class for center-aligning text horizontally.
    /// </summary>
    public const string CenterText = "text-center";

    /// <summary>
    /// CSS class for right-aligning text.
    /// </summary>
    public const string RightAlign = "text-end";

    /// <summary>
    /// CSS class for action button columns (edit, delete, etc.).
    /// </summary>
    public const string Button = "text-center";

    /// <summary>
    /// CSS class for selecting the no-wrap behavior on text.
    /// </summary>
    public const string NoWrap = "text-nowrap";

    /// <summary>
    /// CSS class for defining a narrow width column (typically for IDs or checkboxes).
    /// </summary>
    public const string Narrow = "text-center";

    /// <summary>
    /// CSS class for emphasizing cell content (e.g., bold or styled).
    /// </summary>
    public const string Emphasis = "fw-bold";

    /// <summary>
    /// CSS class for muted or secondary emphasis.
    /// </summary>
    public const string Muted = "text-muted";
}
